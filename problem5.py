# Project Euler
# Problem 5

# Solved by reasoning

# The smallest positive number divisible by the first 20 positive integers
# can be found by multiplying the factors together.

# Starting with 1 and adding factors to create the first 20 numbers, the solution is
# 1 * 2 * 3 * 2 * 5 * 7 * 4 * 3 * 11 * 13 * 17 * 19 = 232792560

# Numbers like 12, 14, 15, 16, etc can be skipped because they already "exist" as factors (ie 12 = 2 * 3 * 2)

# There is a programming solution, but a solution is a solution

# Answer 232792560





